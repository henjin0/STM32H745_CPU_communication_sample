/**
 * @brief  The application entry point.
 * @retval int
 */
int main(void) {
	/* USER CODE BEGIN 1 */
	uint32_t time, t1, t2;
	/* USER CODE END 1 */
	/* USER CODE BEGIN Boot_Mode_Sequence_0 */
	int32_t timeout;
	/* USER CODE END Boot_Mode_Sequence_0 */

	/* USER CODE BEGIN Boot_Mode_Sequence_1 */
	/* Wait until CPU2 boots and enters in stop mode or timeout*/
	timeout = 0xFFFF;
	while ((__HAL_RCC_GET_FLAG(RCC_FLAG_D2CKRDY) != RESET) && (timeout-- > 0))
		;
	if (timeout < 0) {
		Error_Handler();
	}
	/* USER CODE END Boot_Mode_Sequence_1 */
	/* MCU Configuration--------------------------------------------------------*/

	/* Reset of all peripherals, Initializes the Flash interface and the Systick. */
	HAL_Init();

	/* USER CODE BEGIN Init */

	/* USER CODE END Init */

	/* Configure the system clock */
	SystemClock_Config();
	/* USER CODE BEGIN Boot_Mode_Sequence_2 */
	/* When system initialization is finished, Cortex-M7 will release Cortex-M4 by means of
	 HSEM notification */
	/* Initialize buffers that are used as shared memory */
	ringbuff_init(rb_cm7_to_cm4, (void*) BUFFDATA_CM7_TO_CM4_ADDR,
	BUFFDATA_CM7_TO_CM4_LEN);
	ringbuff_init(rb_cm4_to_cm7, (void*) BUFFDATA_CM4_TO_CM7_ADDR,
	BUFFDATA_CM4_TO_CM7_LEN);

	/*HW semaphore Clock enable*/
	__HAL_RCC_HSEM_CLK_ENABLE();
	/*Take HSEM */
	HAL_HSEM_FastTake(HSEM_ID_0);
	/*Release HSEM in order to notify the CPU2(CM4)*/
	HAL_HSEM_Release(HSEM_ID_0, 0);
	/* wait until CPU2 wakes up from stop mode */
	timeout = 0xFFFF;
	while ((__HAL_RCC_GET_FLAG(RCC_FLAG_D2CKRDY) == RESET) && (timeout-- > 0))
		;
	if (timeout < 0) {
		Error_Handler();
	}
	/* USER CODE END Boot_Mode_Sequence_2 */

	/* USER CODE BEGIN SysInit */

	/* USER CODE END SysInit */

	/* Initialize all configured peripherals */
	MX_GPIO_Init();
	MX_USART3_UART_Init();
	MX_USART2_UART_Init();
	MX_UART4_Init();
	/* USER CODE BEGIN 2 */
	/* USER CODE END 2 */

	/* Infinite loop */
	/* USER CODE BEGIN WHILE */
	/* Infinite loop */
	/* USER CODE BEGIN WHILE */
	//HAL_UART_Transmit(&huart3, (void*) "[CM7] Core ready\r\n", 18, 100);
	while (!ringbuff_is_ready(rb_cm4_to_cm7)
			|| !ringbuff_is_ready(rb_cm7_to_cm4)) {
	}

	/* Set default time */
	time = t1 = t2 = HAL_GetTick();
	char *str;
	uint32_t i = 0;

	HAL_UART_Receive_IT(&huart4, RxData, UART_DATALEN);
	receiveFlag = FALSE;

	char sendOP[4][9]={"STOP____","WALK____","SIDESTR_","TURNL___"};

	int counter = 0;
	while (1) {
		size_t len;
		void *addr;

		time = HAL_GetTick();

		/* Check if CPU2 sent some data to CPU1 core */
		while ((len = ringbuff_get_linear_block_read_length(rb_cm4_to_cm7)) > 0) {
			addr = ringbuff_get_linear_block_read_address(rb_cm4_to_cm7);
			str = addr;
			/* Transmit data */
			if (receiveFlag) {
				ringbuff_write(rb_cm7_to_cm4, RxData, UART_DATALEN);
				receiveFlag = FALSE;
				HAL_UART_Receive_IT(&huart4, &RxData, UART_DATALEN);
			}

			/* Mark buffer as read */
			ringbuff_skip(rb_cm4_to_cm7, len);
		}

		/* Toggle LED */
		if (time - t1 >= 500) {
			t1 = time;
			HAL_GPIO_TogglePin(GPIOB, GPIO_PIN_0);
		}

		if(time - t2 >= 3000){
			t2 = time;
			counter = counter + 1;
			ringbuff_write(rb_cm7_to_cm4, sendOP[counter], UART_DATALEN);
		}
		if(counter >= 3)
			counter = 0;


	}
	/* USER CODE END 3 */
	/* USER CODE END 3 */
}

